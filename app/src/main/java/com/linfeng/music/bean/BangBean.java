package com.linfeng.music.bean;

public class BangBean {
    private String bangName;
    private String bangImgUrl;
    private String bangId;

    public String getBangName() {
        return bangName;
    }

    public void setBangName(String bangName) {
        this.bangName = bangName;
    }

    public String getBangImgUrl() {
        return bangImgUrl;
    }

    public void setBangImgUrl(String bangImgUrl) {
        this.bangImgUrl = bangImgUrl;
    }

    public String getBangId() {
        return bangId;
    }

    public void setBangId(String bangId) {
        this.bangId = bangId;
    }
}
